int ft_sqrt(int nb)
{
	int n;

	n = 0;
	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	else if (nb > 1)
	{
		while (n <= 47000)
		{
			if (n * n == nb)
				return (n);
			n++;
		}
	}
	else 
		return (0);
	return (0);
}

