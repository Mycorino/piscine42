int	ft_str_is_alpha(char *str)
{
	int i;
	int b;

	i = 0;
	b = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'a' && str[i] <= 'z') && !((str[i] >= 'A' &&
			str[i] <= 'Z')))
			b = 0;
		i++;
	}
	return (b);
}
